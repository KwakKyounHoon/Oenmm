package com.onemeter.omm.onemm.data;

public class SettingSave {
    private String listeningProfit;
    private String payTotal;
    private String withdrawTotal;
    private String questionCost;
    private String currentPoint;
    private String listeningCost;

    public String getListeningProfit() {
        return this.listeningProfit;
    }

    public void setListeningProfit(String listeningProfit) {
        this.listeningProfit = listeningProfit;
    }

    public String getPayTotal() {
        return this.payTotal;
    }

    public void setPayTotal(String payTotal) {
        this.payTotal = payTotal;
    }

    public String getWithdrawTotal() {
        return this.withdrawTotal;
    }

    public void setWithdrawTotal(String withdrawTotal) {
        this.withdrawTotal = withdrawTotal;
    }

    public String getQuestionCost() {
        return this.questionCost;
    }

    public void setQuestionCost(String questionCost) {
        this.questionCost = questionCost;
    }

    public String getCurrentPoint() {
        return this.currentPoint;
    }

    public void setCurrentPoint(String currentPoint) {
        this.currentPoint = currentPoint;
    }

    public String getListeningCost() {
        return this.listeningCost;
    }

    public void setListeningCost(String listeningCost) {
        this.listeningCost = listeningCost;
    }
}
