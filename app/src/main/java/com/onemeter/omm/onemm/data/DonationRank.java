package com.onemeter.omm.onemm.data;

public class DonationRank {
    private String donationName;
    private String nickname;
    private String name;
    private String photo;
    private String donationId;
    private String donationPhoto;
    private String userId;

    public String getDonationName() {
        return this.donationName;
    }

    public void setDonationName(String donationName) {
        this.donationName = donationName;
    }

    public String getNickname() {
        return this.nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoto() {
        return this.photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getDonationId() {
        return this.donationId;
    }

    public void setDonationId(String donationId) {
        this.donationId = donationId;
    }

    public String getDonationPhoto() {
        return this.donationPhoto;
    }

    public void setDonationPhoto(String donationPhoto) {
        this.donationPhoto = donationPhoto;
    }

    public String getUserId() {
        return this.userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
